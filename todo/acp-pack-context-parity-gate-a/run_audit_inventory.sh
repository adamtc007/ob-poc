#!/usr/bin/env bash
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
cd "$ROOT"

echo "# ACP Pack Context Parity Gate A Inventory"
echo
echo "Generated by: \`todo/acp-pack-context-parity-gate-a/run_audit_inventory.sh\`"
echo

echo "## Repository State"
echo
echo "\`\`\`text"
git status --short
echo "\`\`\`"
echo

echo "## Config Surface Counts"
echo
printf -- "- Verb YAML files: "
find rust/config/verbs -type f \( -name '*.yaml' -o -name '*.yml' \) | wc -l | tr -d ' '
printf -- "- Journey pack YAML files: "
find rust/config/packs -type f \( -name '*.yaml' -o -name '*.yml' \) | wc -l | tr -d ' '
printf -- "- SemOS seed YAML files: "
find rust/config/sem_os_seeds -type f \( -name '*.yaml' -o -name '*.yml' \) | wc -l | tr -d ' '
printf -- "- Stategraph YAML files: "
find rust/config/stategraphs rust/config/sem_os_seeds/state_machines -type f \( -name '*.yaml' -o -name '*.yml' \) | wc -l | tr -d ' '
printf -- "- Macro schema/registry YAML files: "
find rust/config/verb_schemas/macros rust/config/macros -type f \( -name '*.yaml' -o -name '*.yml' \) | wc -l | tr -d ' '
printf -- "- Workflow YAML files: "
find rust/config/workflows -type f \( -name '*.yaml' -o -name '*.yml' \) | wc -l | tr -d ' '
echo

echo "## Slice 1 Pack Allowed Verb Counts"
echo
for pack in onboarding-request cbu-maintenance product-service-taxonomy; do
  file="rust/config/packs/${pack}.yaml"
  count="$(awk '
    /^allowed_verbs:/ {inside=1; next}
    /^[^[:space:]-]/ && inside {inside=0}
    inside && /^[[:space:]]*-[[:space:]]/ {count++}
    END {print count+0}
  ' "$file")"
  echo "- ${pack}: ${count}"
done
echo

echo "## Route Surface Hints"
echo
echo "\`\`\`text"
rg -n "route\\(|/api/session|/api/repl|utterance|try_route|resolve_acp_dag_semantic_prompt|execute_session_dsl_legacy_raw_only" \
  rust/src/api rust/src/acp_protocol.rs rust/src/acp_dag_semantic.rs -S
echo "\`\`\`"
echo

echo "## Public Symbol Counts By Source Root"
echo
echo "\`\`\`text"
for dir in rust/src rust/crates/*/src; do
  [ -d "$dir" ] || continue
  count="$(rg -n "^[[:space:]]*pub(\\(|[[:space:]])" "$dir" -g '*.rs' | wc -l | tr -d ' ')"
  printf "%-60s %s\n" "$dir" "$count"
done
echo "\`\`\`"
echo

echo "## Workspace Members"
echo
echo "\`\`\`text"
cargo metadata --format-version 1 --no-deps --manifest-path rust/Cargo.toml \
  | tr ',' '\n' \
  | rg '"name"|"manifest_path"'
echo "\`\`\`"

