use anyhow::{bail, Context, Result};
use regex::Regex;
use std::collections::BTreeSet;
use std::fs;
use std::path::{Path, PathBuf};

const ALLOWLIST_PATH: &str = "tools/public-api-allowlist.txt";
const SCANNED_FILES: &[&str] = &[
    "src/lib.rs",
    "src/acp_dag_semantic.rs",
    "src/acp_pack_context_envelope_v2.rs",
    "src/acp_registry_projection.rs",
    "src/acp_runtime_context.rs",
    "src/acp_static_context_acceptance.rs",
];

/// Run the public API allowlist check.
///
/// # Examples
///
/// ```rust,no_run
/// xtask::pub_lint::run(false).unwrap();
/// ```
pub(crate) fn run(bless: bool) -> Result<()> {
    let observed = collect_public_api_items(Path::new("."))?;
    if bless {
        write_allowlist(Path::new(ALLOWLIST_PATH), &observed)?;
        println!(
            "Public API allowlist refreshed with {} items at {ALLOWLIST_PATH}",
            observed.len()
        );
        return Ok(());
    }

    let expected = read_allowlist(Path::new(ALLOWLIST_PATH))?;
    let added = observed.difference(&expected).cloned().collect::<Vec<_>>();
    let removed = expected.difference(&observed).cloned().collect::<Vec<_>>();

    if added.is_empty() && removed.is_empty() {
        println!(
            "Public API allowlist clean: {} checked items across {} files",
            observed.len(),
            SCANNED_FILES.len()
        );
        return Ok(());
    }

    if !added.is_empty() {
        eprintln!("New public API items not present in {ALLOWLIST_PATH}:");
        for item in &added {
            eprintln!("  + {item}");
        }
    }
    if !removed.is_empty() {
        eprintln!("Public API allowlist entries no longer observed:");
        for item in &removed {
            eprintln!("  - {item}");
        }
    }
    bail!("public API allowlist drift detected; run `cargo run -p xtask -- pub-lint --bless` only after reviewing the boundary change")
}

fn collect_public_api_items(root: &Path) -> Result<BTreeSet<String>> {
    let matcher = Regex::new(
        r"^\s*pub\s+(?:async\s+|unsafe\s+|extern\s+)?(?:mod|use|struct|enum|trait|type|const|static|fn)\b",
    )
    .expect("public API regex should compile");
    let mut items = BTreeSet::new();
    for relative_path in SCANNED_FILES {
        let path = root.join(relative_path);
        let source = fs::read_to_string(&path)
            .with_context(|| format!("reading public API scan target {}", path.display()))?;
        for line in source.lines() {
            let trimmed = line.trim();
            if matcher.is_match(trimmed) {
                items.insert(format!(
                    "{}:{}",
                    normalize_path(relative_path),
                    normalize_public_line(trimmed)
                ));
            }
        }
    }
    Ok(items)
}

fn read_allowlist(path: &Path) -> Result<BTreeSet<String>> {
    let source = fs::read_to_string(path)
        .with_context(|| format!("reading public API allowlist {}", path.display()))?;
    Ok(source
        .lines()
        .map(str::trim)
        .filter(|line| !line.is_empty() && !line.starts_with('#'))
        .map(str::to_string)
        .collect())
}

fn write_allowlist(path: &Path, items: &BTreeSet<String>) -> Result<()> {
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).with_context(|| {
            format!(
                "creating public API allowlist directory {}",
                parent.display()
            )
        })?;
    }
    let mut output = String::new();
    output.push_str("# Public API allowlist for the root crate ACP boundary.\n");
    output.push_str("# Generated by `cargo run -p xtask -- pub-lint --bless` after review.\n");
    output.push_str("# Do not edit casually; new entries are API-surface changes.\n\n");
    for item in items {
        output.push_str(item);
        output.push('\n');
    }
    fs::write(path, output)
        .with_context(|| format!("writing public API allowlist {}", path.display()))
}

fn normalize_path(path: &str) -> String {
    PathBuf::from(path).display().to_string()
}

fn normalize_public_line(line: &str) -> String {
    line.split_whitespace().collect::<Vec<_>>().join(" ")
}
