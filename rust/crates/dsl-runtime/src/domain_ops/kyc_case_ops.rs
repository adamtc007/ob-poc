/// Terminal statuses allowed when closing a KYC case.
const CLOSE_STATUSES: &[&str] = &[
    "APPROVED",
    "REJECTED",
    "WITHDRAWN",
    "EXPIRED",
    "REFER_TO_REGULATOR",
    "DO_NOT_ONBOARD",
];

/// Check whether a case status transition is valid against the FSM.
fn is_valid_transition(catalog: &dyn LifecycleCatalog, from: &str, to: &str) -> bool {
    catalog.is_valid_transition("kyc_case", from, to)
}

/// Check whether the target status is a terminal status (must use `close` verb).
fn is_terminal_status(catalog: &dyn LifecycleCatalog, status: &str) -> bool {
    CLOSE_STATUSES.contains(&status) && catalog.is_terminal_state("kyc_case", status)
}

fn valid_next_statuses(catalog: &dyn LifecycleCatalog, status: &str) -> String {
    catalog.valid_next_states("kyc_case", status).join(", ")
}

// ============================================================================
// KycCaseUpdateStatusOp - Validated status transitions
// ============================================================================

/// Updates KYC case status with state machine validation.
///
/// Validates that the requested transition is allowed by the case state machine.
/// Terminal statuses (APPROVED, REJECTED, etc.) are rejected with a message
/// directing callers to use `kyc-case.close` instead.
#[register_custom_op]
pub struct KycCaseUpdateStatusOp;

#[async_trait]
impl CustomOperation for KycCaseUpdateStatusOp {
    fn domain(&self) -> &'static str {
        "kyc-case"
    }

    fn verb(&self) -> &'static str {
        "update-status"
    }

    fn rationale(&self) -> &'static str {
        "Enforces case state machine transitions — prevents skipping compliance stages"
    }



    async fn execute_json(
        &self,
        args: &serde_json::Value,
        ctx: &mut VerbExecutionContext,
        pool: &PgPool,
    ) -> Result<VerbExecutionOutcome> {
        use crate::domain_ops::helpers::json_extract_string;

        let catalog = ctx.service::<dyn LifecycleCatalog>()?;
        let case_id = json_extract_uuid(args, ctx, "case-id")?;
        let requested_status = json_extract_string(args, "status")?;
        let notes = json_extract_string_opt(args, "notes");

        if is_terminal_status(catalog.as_ref(), &requested_status) {
            return Err(anyhow!(
                "Use kyc-case.close for terminal status '{}'. The update-status verb handles non-terminal transitions only.",
                requested_status
            ));
        }

        let current_status: String =
            sqlx::query_scalar(r#"SELECT status FROM "ob-poc".cases WHERE case_id = $1"#)
                .bind(case_id)
                .fetch_optional(pool)
                .await?
                .ok_or_else(|| anyhow!("KYC case not found: {}", case_id))?;

        if !is_valid_transition(catalog.as_ref(), &current_status, &requested_status) {
            return Err(anyhow!(
                "Invalid transition: {} → {}. Allowed transitions from {}: [{}]",
                current_status,
                requested_status,
                current_status,
                valid_next_statuses(catalog.as_ref(), &current_status)
            ));
        }

        sqlx::query(
            r#"UPDATE "ob-poc".cases
               SET status = $2,
                   notes = COALESCE($3, notes),
                   updated_at = NOW()
               WHERE case_id = $1"#,
        )
        .bind(case_id)
        .bind(&requested_status)
        .bind(&notes)
        .execute(pool)
        .await?;

        Ok(VerbExecutionOutcome::Affected(1))
    }

    fn is_migrated(&self) -> bool {
        true
    }
}

// ============================================================================
// KycCaseCloseOp - Close case with deal gate event emission
// ============================================================================

/// Closes a KYC case, setting `closed_at` and terminal status.
///
/// Validates the case is in REVIEW status before allowing closure.
/// When the case is APPROVED and has a linked `deal_id`, emits a
/// `KYC_GATE_COMPLETED` event into `deal_events` so the deal pipeline
/// knows the KYC gate is satisfied.
#[register_custom_op]
pub struct KycCaseCloseOp;

#[async_trait]
impl CustomOperation for KycCaseCloseOp {
    fn domain(&self) -> &'static str {
        "kyc-case"
    }

    fn verb(&self) -> &'static str {
        "close"
    }

    fn rationale(&self) -> &'static str {
        "Validates REVIEW status, sets closed_at, and emits KYC_GATE_COMPLETED deal event when APPROVED with linked deal"
    }



    async fn execute_json(
        &self,
        args: &serde_json::Value,
        ctx: &mut VerbExecutionContext,
        pool: &PgPool,
    ) -> Result<VerbExecutionOutcome> {
        use crate::domain_ops::helpers::json_extract_string;

        let case_id = json_extract_uuid(args, ctx, "case-id")?;
        let status = json_extract_string(args, "status")?;
        let notes = json_extract_string_opt(args, "notes");

        if !CLOSE_STATUSES.contains(&status.as_str()) {
            return Err(anyhow!(
                "Invalid close status '{}'. Must be one of: {}",
                status,
                CLOSE_STATUSES.join(", ")
            ));
        }

        let result = kyc_case_close_impl(case_id, &status, notes.as_deref(), pool).await?;
        Ok(VerbExecutionOutcome::Record(serde_json::to_value(result)?))
    }

    fn is_migrated(&self) -> bool {
        true
    }
}

/// Shared implementation for kyc-case.close.
async fn kyc_case_close_impl(
    case_id: Uuid,
    status: &str,
    notes: Option<&str>,
    pool: &PgPool,
) -> Result<KycCaseCloseResult> {
    // Load current case to validate state and retrieve deal_id
    let row: Option<(String, Option<Uuid>, Option<String>)> = sqlx::query_as(
        r#"SELECT status, deal_id, case_ref
           FROM "ob-poc".cases
           WHERE case_id = $1"#,
    )
    .bind(case_id)
    .fetch_optional(pool)
    .await?;

    let (current_status, deal_id, case_ref) =
        row.ok_or_else(|| anyhow!("KYC case not found: {}", case_id))?;

    // Validate the case is in REVIEW status (closeable state)
    if current_status != "REVIEW" {
        return Err(anyhow!(
            "KYC case {} is in status '{}', but must be in REVIEW to close",
            case_id,
            current_status
        ));
    }

    // Close the case: set status and closed_at
    let closed_at: String = sqlx::query_scalar(
        r#"UPDATE "ob-poc".cases
           SET status = $2,
               closed_at = NOW(),
               notes = COALESCE($3, notes),
               updated_at = NOW()
           WHERE case_id = $1
           RETURNING to_char(closed_at, 'YYYY-MM-DD"T"HH24:MI:SS"Z"')"#,
    )
    .bind(case_id)
    .bind(status)
    .bind(notes)
    .fetch_one(pool)
    .await?;

    // If APPROVED and deal_id is present, emit KYC_GATE_COMPLETED event
    let mut deal_gate_updated = false;

    if status == "APPROVED" {
        if let Some(did) = deal_id {
            let event_id = Uuid::new_v4();
            let detail = json!({
                "case_id": case_id,
                "case_ref": case_ref,
                "approved_at": closed_at,
                "gate": "KYC"
            });

            sqlx::query(
                r#"INSERT INTO "ob-poc".deal_events
                       (event_id, deal_id, event_type, subject_type, subject_id, new_value, description)
                   VALUES ($1, $2, 'KYC_GATE_COMPLETED', 'KYC_CASE', $3, 'APPROVED', $4)"#,
            )
            .bind(event_id)
            .bind(did)
            .bind(case_id)
            .bind(detail.to_string())
            .execute(pool)
            .await?;

            deal_gate_updated = true;
        }
    }

    Ok(KycCaseCloseResult {
        case_id,
        status: status.to_string(),
        closed_at,
        deal_gate_updated,
    })
}

