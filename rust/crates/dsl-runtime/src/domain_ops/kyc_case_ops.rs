    /// (only wired up inside the host binary via the ServiceRegistry).
    struct TestKycLifecycleCatalog;

    impl TestKycLifecycleCatalog {
        const TERMINALS: &'static [&'static str] = &[
            "APPROVED",
            "REJECTED",
            "WITHDRAWN",
            "EXPIRED",
            "REFER_TO_REGULATOR",
            "DO_NOT_ONBOARD",
        ];

        fn next_states(state: &str) -> &'static [&'static str] {
            match state {
                "INTAKE" => &["DISCOVERY", "WITHDRAWN"],
                "DISCOVERY" => &["ASSESSMENT", "BLOCKED", "WITHDRAWN"],
                "ASSESSMENT" => &["REVIEW", "BLOCKED", "WITHDRAWN"],
                "REVIEW" => &[
                    "APPROVED",
                    "REJECTED",
                    "BLOCKED",
                    "WITHDRAWN",
                    "REFER_TO_REGULATOR",
                    "DO_NOT_ONBOARD",
                    "EXPIRED",
                ],
                "BLOCKED" => &["DISCOVERY", "ASSESSMENT", "REVIEW"],
                _ => &[],
            }
        }
    }

    impl LifecycleCatalog for TestKycLifecycleCatalog {
        fn is_valid_transition(&self, entity_type: &str, from: &str, to: &str) -> bool {
            if entity_type != "kyc_case" {
                return false;
            }
            Self::next_states(from).iter().any(|s| *s == to)
        }

        fn is_terminal_state(&self, entity_type: &str, state: &str) -> bool {
            if entity_type != "kyc_case" {
                return false;
            }
            Self::TERMINALS.contains(&state)
        }

        fn valid_next_states(&self, entity_type: &str, state: &str) -> Vec<String> {
            if entity_type != "kyc_case" {
                return Vec::new();
            }
            Self::next_states(state).iter().map(|s| s.to_string()).collect()
        }
    }

